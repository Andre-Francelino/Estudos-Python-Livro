# Operadores e operações matémáticas
"""
    +     Adição
    -     Subtração
    *     Multiplicação
    /     Divisão (com resultado fracionário)
    //    Divisão (com resultados inteiros)
    %     Módulo ou restoP
    **    Exponenciação ou potenciação  
"""
# + Adição
print(2 + 3)

# - Subtração
print(5 - 3)
print(10 - 4 + 2) # podemos combinar adição e subtração

# *  Multiplicação
print(2 * 10)

# /  Divisão (com resultado fracionário)
print(20 / 4)

# // Divisão (com resultados inteiros)
print(20 // 4)

# %  Módulo ou resto
print(10 % 3)
print(16 % 7)
print(63 % 8)

# ** Exponenciação ou potenciação
print(2 ** 3)

"""
Precedência nas operações:
    1 Exponeciação ou potenciação
    2 Multiplicação, divisão e módulo
    3 Adição e subtração
"""
expressao = 1500 + (1500 * 5 / 100)
print(expressao)
